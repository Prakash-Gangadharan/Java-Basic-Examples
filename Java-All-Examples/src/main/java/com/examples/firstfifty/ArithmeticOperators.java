package com.examples.firstfifty;

class ArithmeticOperators {
	public static void main(String args[]) {
		int a = 2;
		int b = 5;
		double c = 5.0;

		int e = b / a;
		double f = c / a;

		float g = b % a;
		double h = c % a;
		System.out.println("\n e:" + e + "  f:" + f + "  g:" + g + "  h:" + h);
	}
}